function return_common(list_of, count) {
    var d = {}
    var common_list = []
    for (let i = 0; i < list_of.length; i++){
      if (list_of[i] in d){
        d[list_of[i]] +=1
        if (d[list_of[i]] == count){
          common_list.push(list_of[i])
        }
      }
      else {
        d[list_of[i]] = 1
      }
    }
    return common_list
}
